class Product{
    brand
    name
    region
    age
    price
    sale_price
    type
    country
    abv
    qty
    img_link
    description
    index

    constructor(brand,name,region,age,price,sale_price,type,country,abv,img_link,description,index){
        this.brand=brand
        this.name=name
        this.region=region
        this.age=age
        this.price=price
        this.sale_price=sale_price
        this.type=type
        this.country=country
        this.abv=abv
        this.img_link=img_link
        this.description=description
        this.qty=0
        this.index=index
        
    }
}